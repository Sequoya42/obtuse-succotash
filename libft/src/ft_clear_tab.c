#include "libft.h"

void		ft_clear_tab(char ***t)
{
	int		i;

	i = 0;
	if (*t != NULL)
	{
		while ((*t)[i])
		{
			free((*t)[i]);
			(*t)[i] = NULL;
			i++;
		}
		free(*t);
		*t = NULL;
	}
}
