#include "libft.h"

int			ft_nb_tab(char **t)
{
	int		i;

	i = 0;
	while (t[i])
		i++;
	return (i);
}

void		ft_init_tab(char **t)
{
	int		i;

	i = 0;
	while (t[i])
	{
		t[i] = NULL;
		++i;
	}
	t[i] = NULL;
}
